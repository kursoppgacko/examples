//imena klasa se pisu velikim slovima
public class Main {

    //metoda main je pocetna tacka svakog programa
    //postoji samo jedna metoda main kao ulazna tacka u svakom programu
    //imena metoda se pisu malim slovima
    public static void main(String[] args) {
        //naredba kojom se ispisuje tekst tj. string Hello World na konzoli(terminalu)
        //naredbom se moze nazvati svaka linija koda u formi IZRAZ_NAREDBE;
        //na kraju svake naredbe stoji ; koja oznacava kraj naredbe
        System.out.println("Hello World");
    }
}
